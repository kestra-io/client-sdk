import { execFileSync } from "node:child_process";
import { mkdtemp, mkdir, readFile, rm, writeFile } from "node:fs/promises";
import os from "node:os";
import path from "node:path";
import { describe, expect, it } from "vitest";

const scriptPath = path.resolve(import.meta.dirname, "../javascript-sdk/scripts/convert-openapi-sdk-functions.mjs");

const read = async (filePath: string) => readFile(filePath, "utf8");

describe("convert-openapi-sdk-functions", () => {
    it("converts only exported SDK arrow functions into function declarations", async () => {
        const tempRoot = await mkdtemp(path.join(os.tmpdir(), "sdk-fn-rewrite-"));
        const openApiRoot = path.join(tempRoot, "src", "openapi");

        try {
            const sdkDir = path.join(openApiRoot, "sdk");
            const otherDir = openApiRoot;
            await mkdir(sdkDir, { recursive: true });
            await mkdir(otherDir, { recursive: true });

            const sdkFile = path.join(sdkDir, "example.gen.ts");
            const otherFile = path.join(otherDir, "client.gen.ts");

            await writeFile(
                sdkFile,
                `// This file is auto-generated by @hey-api/openapi-ts

export const keepClient = createClient(createConfig());

/**
 * Example operation.
 */
export const convertMe = <T>(value: T) => {
    const result = value;
    return result;
};

export const trimValue = (value: string) => value.trim();

const helper = () => 1;
`,
            );
            await writeFile(otherFile, `export const keepMe = () => "client";\n`);

            execFileSync("node", [scriptPath, openApiRoot], { stdio: "pipe" });

            const rewrittenSdkFile = await read(sdkFile);
            const untouchedOtherFile = await read(otherFile);

            expect(rewrittenSdkFile).toContain("export function convertMe<T>(value: T) {");
            expect(rewrittenSdkFile).toContain("export function trimValue(value: string) {");
            expect(rewrittenSdkFile).toContain("return value.trim();");
            expect(rewrittenSdkFile).toContain("export const keepClient = createClient(createConfig());");
            expect(rewrittenSdkFile).toContain("const helper = () => 1;");
            expect(rewrittenSdkFile).not.toContain("export const convertMe");
            expect(rewrittenSdkFile).not.toContain("export const trimValue");

            expect(untouchedOtherFile).toBe(`export const keepMe = () => "client";\n`);
        } finally {
            await rm(tempRoot, { recursive: true, force: true });
        }
    });
});
