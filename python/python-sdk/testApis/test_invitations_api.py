import unittest
from kestrapy import (
    Configuration,
    KestraClient,
    IAMInvitationControllerApiInvitationCreateRequest,
    QueryFilter,
    QueryFilterField,
    QueryFilterOp,
)


class TestInvitationsApi(unittest.TestCase):
    """InvitationsApi unit test stubs"""

    def setUp(self) -> None:
        configuration = Configuration()
        configuration.host = "http://localhost:9902"
        configuration.username = "root@root.com"
        configuration.password = "Root!1234"

        self.kestra_client = KestraClient(configuration)
        self.tenant = "main"

    def test_search_invitations_by_email_filter(self) -> None:
        """Search invitations using email filter"""
        email = f"test_invitation_filter_{id(self)}@kestra.io"

        self.kestra_client.invitations.create_invitation(
            tenant=self.tenant,
            iam_invitation_controller_api_invitation_create_request=IAMInvitationControllerApiInvitationCreateRequest(
                email=email
            )
        )

        qf = QueryFilter(field=QueryFilterField.EMAIL, operation=QueryFilterOp.EQUALS, value={"value": email})
        page = self.kestra_client.invitations.search_invitations(
            page=1,
            size=50,
            filters=[qf],
            tenant=self.tenant
        )
        assert any(getattr(i, "email", None) == email for i in page.results)

        invitations = self.kestra_client.invitations.list_invitations_by_email(email=email, tenant=self.tenant)
        for invitation in invitations:
            self.kestra_client.invitations.delete_invitation(id=invitation.id, tenant=self.tenant)
